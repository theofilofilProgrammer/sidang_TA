                <h3 class="main-content-title col-12 px-0 text-left">Inovasi Melalui Kekayaan Intelektual</h3>
                <hr class="mb-4 mt-1 px-0 col-12">
                <p class="main-content-paragraph text-justify col-12 px-0">Hak Kekayaan Intelektual (HKI), yang kini lebih sering disebut Kekayaan Intelektual (KI), adalah hak eksklusif yang diberikan oleh hukum kepada seseorang atau kelompok atas karya yang dihasilkan dari kemampuan intelektual manusia, seperti dalam bidang seni, sastra, ilmu pengetahuan, dan teknologi. Di Indonesia, pengelolaan HKI berada di bawah Direktorat Jenderal Kekayaan Intelektual (DJKI), yang bernaung di Kementerian Hukum dan Hak Asasi Manusia (Kemenkumham).</p>
                <p class="main-content-paragraph text-justify col-12 px-0">Pendaftaran KI di Universitas Telkom sesuai peraturan (regulasi) yang berlaku, wajib didaftarkan melalui Unit KI dan Transfer Teknologi Bandung Techno Park melalui aplikasi MyBTP menggunakan SSO Dosen / Pegawai.</p>
                <p class="main-content-paragraph text-justify col-12 px-0">Program Studi D3 Teknologi Informasi PDSKU Politeknik Negeri Pontianak di Kabupaten Sukamara mendorong sivitas akademiknya untuk menghasilkan karya-karya inovatif yang dilindungi sebagai Kekayaan Intelektual (HKI) dan Paten. Perlindungan HKI adalah bentuk apresiasi terhadap kreativitas dan inovasi, serta menjadi jembatan antara hasil riset akademik dengan aplikasi nyata di masyarakat dan industri.</p>
                <p class="main-content-paragraph text-justify col-12 px-0">Halaman ini menampilkan sebagian dari portofolio HKI dan Paten yang telah dihasilkan oleh dosen dan mahasiswa kami, mencakup berbagai bidang mulai dari perangkat lunak, sistem informasi, hingga desain industri. Kami berkomitmen untuk terus meningkatkan kontribusi dalam pengembangan ilmu pengetahuan dan teknologi melalui inovasi yang terlindungi.</p>

                <h3 class="main-content-title col-12 px-0 text-left">Penjelasan Kekayaan Intelektual (KI)</h3>
                <hr class="mb-4 mt-1 px-0 col-12">
                <p class="main-content-paragraph text-justify col-12 px-0">Definisi KI mencakup karya-karya yang lahir dari kreativitas intelektual manusia, seperti penemuan, karya seni, sastra, teknologi, simbol, nama, dan gambar yang digunakan dalam perdagangan.</p>

                <h3 class="main-content-title col-12 px-0 text-left">Kategori Utama Kekayaan Intelektual</h3>
                <hr class="mb-4 mt-1 px-0 col-12">
                <h5 class="main-content-subtitle col-12 px-0 mt-0 text-left">Hak Cipta</h5>
                <p class="main-content-paragraph text-justify col-12 px-0">Hak eksklusif pencipta atas karya seperti ilmu pengetahuan, seni, sastra, dan program komputer, yang timbul secara otomatis setelah karya diwujudkan dalam bentuk nyata berdasarkan prinsip deklaratif.</p>
                <h5 class="main-content-subtitle col-12 px-0 text-left">Hak Kekayaan Industri</h5>
                <p class="main-content-paragraph col-12 px-0 text-justify">Meliputi paten, merek, desain industri, indikasi geografis, rahasia dagang, desain tata letak sirkuit terpadu, dan perlindungan varietas tanaman.</p>

                <h3 class="main-content-title col-12 px-0 text-left">Tujuan Kekayaan Intelektual</h3>
                <hr class="mb-4 mt-1 px-0 col-12">
                <ul class="main-content-list kekayaan-intelektual-list text-justify col-12 px-0">
                    <li>Melindungi Reputasi dan Inovasi : Memberikan perlindungan hukum kepada pencipta agar karya mereka tidak disalahgunakan, serta mendorong inovasi melalui insentif.</li>
                    <li>Meningkatkan Ekonomi : KI mendukung pertumbuhan ekonomi kreatif, investasi di sektor inovasi, dan daya saing nasional.</li>
                    <li>Mencegah Duplikasi : Memastikan karya tidak ditiru tanpa izin, menghindari risiko pelanggaran.</li>
                    <li>Mendukung Kebudayaan dan Sosial : Melindungi karya seni dan ilmu pengetahuan untuk meningkatkan taraf hidup masyarakat.</li>
                </ul>

                <h3 class="main-content-title col-12 px-0 text-left">Dasar Hukum</h3>
                <hr class="mb-4 mt-1 px-0 col-12">
                <ul class="main-content-list kekayaan-intelektual-list text-justify col-12 px-0">
                    <li>KI di Indonesia diatur dalam berbagai undang-undang, seperti UU No. 28 Tahun 2014 tentang Hak Cipta, UU No. 14 Tahun 2001 tentang Paten, dan UU No. 15 Tahun 2001 tentang Merek.</li>
                    <li>Indonesia juga menyesuaikan regulasi dengan standar internasional seperti TRIPs (Trade-Related Aspects of Intellectual Property Rights) sebagai anggota WTO.</li>
